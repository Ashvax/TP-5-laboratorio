    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:

        String nombre = jtfNombre.getText();
        float precio = 0;
        String categoria = jcbCategoria.getSelectedItem().toString();
        boolean flag = false;

        if (!jtfNombre.getText().equalsIgnoreCase("") && !jtfPrecio.getText().equalsIgnoreCase("")) {
            try {
                precio = Float.parseFloat(jtfPrecio.getText());
            } catch (Exception e) {
                JOptionPane.showMessageDialog(this, "No s√© ingreso un precio valido");
                jtfPrecio.requestFocus();
                flag = true;
            }
        }else{
            JOptionPane.showMessageDialog(this,"Debe llenar todos los campos antes de agregar");
            flag=true;
        }

        if (!flag) {
            productos.add(new Producto(categoria, nombre, precio));
            String[] col = {"Categoria", "Nombre", "Precio"};
            DefaultTableModel tm = new DefaultTableModel(col, 0);

            for (Producto p : productos) {
                Object[] data = {p.getCategoria(), p.getNombre(), "US$ "+p.getPrecio()};
                tm.addRow(data);
            }

            jtblProductos.setModel(tm);
            jtfNombre.setText("");
            jtfPrecio.setText("");

            jtfNombre.requestFocus();
        }

    }//GEN-LAST:event_jButton1ActionPerformed

